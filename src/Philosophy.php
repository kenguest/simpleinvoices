<h4 class=CHeading>Free software</h4>
<p class=CParagraph>
For Simple Invoices, the "free" in "free software" is used primarily in reference to
freedom and
not to price - although we are committed to not charging for Simple Invoices. The most
important
thing about Simple Invoices is not that it is available free of charge, but that it
confers rights
of software freedom on the people who install and use it. It is those freedoms that enable
the Simple Invoices community to grow, sharing its collective experience and expertise to
improve
Simple Invoices and make it suitable for use in new countries and new industries.
<br><br>

Quoting the <a href="http://www.fsf.org/">Free Software Foundation's</a> <a
href="http://www.gnu.org/philosophy/free-sw.html"><i>"What is Free Software"</i></a>, the
freedoms at the core
of free software are defined as:
<br>
<br>&nbsp;&nbsp;&nbsp;&nbsp; * The freedom to run the program, for any purpose.
<br>&nbsp;&nbsp;&nbsp;&nbsp; * The freedom to study how the program works, and adapt it to your needs.
<br>&nbsp;&nbsp;&nbsp;&nbsp; * The freedom to redistribute copies so you can help others.
<br>&nbsp;&nbsp;&nbsp;&nbsp; * The freedom to improve the program, and release your improvements to the public, so
that everyone benefits. 
<br><br>

Free software has been a coherent social movement for more than two decades. This movement
has produced millions of lines of code, documentation, and a vibrant community of which
Simepl Invoices is proud to be a part.
<br><br>
</p>
<h4 class=CHeading>Open source</h4>
<p class="CParagraph">
Open source is a term coined in 1998 to remove the ambiguity in the English word "free".
The <a href="http://www.opensource.org/">Open Source Initiative</a> described open source
software in the <a href="http://www.opensource.org/docs/definition.php">Open Source
Definition</a>.

<br><br>
</p>
<h4 class=CHeading>Community</h4>
<p class=CParagraph>

Simple Invoices is primarily developed by its users and for its users, in an open process. The
goal of the project is to make an invoicing package wich is easy to use, easy to adopt and
which
can reach a community of users that would go far beyond its developers. 
