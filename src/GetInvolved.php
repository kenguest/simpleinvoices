

<div class="CFile CTopic" id=MainTopic><h1 class=CTitle><a name="Get Involved"></a>Get Involved</h1>


<div class=CBody><p class=CParagraph>The best way to get involved in the Simple Invoices project is to download and install Simple Invoices and post your ideas, queries, bugs, documentation, request for help, whatever, in our forums all ideas etc are welcome, the more the better !</p><p class=CParagraph>Forums <a href="http://simpleinvoices.org/forum/" class=LURL>http://simpleinvoices.org/forum/</a></p><ul class=CBulletList><li>General Support</li><li>News and Updates</li><li>Feature Requests</li><li>Translation</li></ul>

<h4 class=CHeading>Development</h4>
<p class=CParagraph>
There are many ways you can help out with the development of Simple Invoices even if you can't code.
<ul class=CBulletList>
	<li>Translations
		<ul class=CBulletList>
			<li>Update an existing translation: Simple Invoices has been translated in a number of languages but not all of these translated are actively maintained (with each new release of Simple Invoices there are new phrases to be translated).  Check of the <a href="http://www.simpleinvoices.org/index.php?page=LangCheck">Lang Check</a> page to find out if there are any translations you can help update
			<li>New translation: Your language is not in the <a href="http://www.simpleinvoices.org/index.php?page=LangCheck">Lang Check</a> page.  That means you are free to create a new translation of Simple Invoices!  Its not to hard, just grab the latest release(or svn if possible) and translate 1 text file (note: there are a few hundred phrases to translate). Refer the Transalation wiki page and forum for more detailed information
			<ul class=CBulletList>
				<li><a href="http://simpleinvoices.org/wiki/doku.php?id=translate_simple_invoices_to_your_language">Translation wiki page and instructions</a>
				<li><a href="http://simpleinvoices.org/forum/forum.php?id=3">Translation Forum</a>
			</ul>
		</ul>

	<li>Helping out on the forums
		<ul class=CBulletList><li>
		Theres always some posts on the forum that can be better answered.  If you can help out answering/resolving someones issue its a great help and a great way to build a stronger community
		<li>Got some useful tips: Feel free to post any tips/updates/whaterver in our forum or blog.
		</ul>
	<li>Code
		<ul class=CBulletList>
			<li>Know or want to know PHP and or MySQL. Jump on the <a href="http://simpleinvoices.org/forum/forum.php?id=3">forum</a> or check out our <a href="http://code.google.com/p/simpleinvoices/issues/list?can=2&q=&sort=priority&colspec=ID%20Type%20Status%20Priority%20Milestone%20Owner%20Summary">issues list</a> to see whats planned for future development. If you can lend a hand coding its always greatly appreciated. Note: for those interested in Simple Invoices development I suggest a quick read of the following links
			<li><a href="http://gettingreal.37signals.com/toc.php">37signals.com :: less features, small and passionate dev teams</a>
			<li><a href="http://simpleinvoices.org/wiki/doku.php?id=simple_invoices_mantra_design_philosophy">Our dev mantra/design philosophy</a>
			<li><a href="http://simpleinvoices.org/index.php?page=Philosophy">What were on about</a>
			<li>Got an idea you want to code but its not on our list or forum. Go for it!!! Just because we havent thought about it or bugged it doesnt mean it shouldnt get done (alot of Simple Invoices dev is stuff someone thought about at 2am in the morning and just started coding). Just keep in mind the above reading: have fun and keep it simple.
		</ul>
	<li>Donate
		<ul class=CBulletList><li>
		To cover the cost of free sofware development donations to Simple Invoices are much appreciated.  Refer the <a href="http://www.simpleinvoices.org/index.php?page=Donate">Donations</a> page for more info.
		</ul>
</ul>

</p>

